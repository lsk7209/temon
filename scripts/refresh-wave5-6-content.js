// ---- 빌더 -------------------------------------------------------------
function buildQuestions(category, label) {
  const scenes = Q_TEMPLATES[category];
  if (!scenes || scenes.length !== 12) {
    throw new Error(`Q_TEMPLATES missing or wrong length for ${category}`);
  }
  return scenes.map((scene, index) => ({
    order: index + 1,
    text: scene.q(label),
    choices: [
      { text: scene.a(label), tags: [scene.tags[0]] },
      { text: scene.b(label), tags: [scene.tags[1]] },
    ],
  }));
}

// IJ/IP/EJ/EP 그룹별 다른 결과 문장 패턴
function groupSummary(group, label, value, tip1, tip2, keyword) {
  const open = `이라 ${label} 상황에서 ${value}${eul(value)} `;
  const tipPart = `${tip1}, ${tip2} 같은 습관을 이어가면 `;
  if (group === "IJ")
    return `${open}흔들리지 않고 자기 기준으로 다룹니다. ${tipPart}${keyword}${eun(keyword)} 더 안정적으로 자리 잡힙니다.`;
  if (group === "IP")
    return `${open}감각에 맞게 유연하게 맞춰 갑니다. ${tipPart}${keyword}${eul(keyword)} 고를 때 더 편안해집니다.`;
  if (group === "EJ")
    return `${open}빠르게 정리하고 실행으로 이어 갑니다. ${tipPart}${keyword} 결정이 명확하고 빠르게 됩니다.`;
  // EP
  return `${open}분위기에 맞게 유연하게 풀어 갑니다. ${tipPart}${keyword}${eul(keyword)} 즐기는 범위가 넓어집니다.`;
}

function buildResults(category, label, mainKeyword) {
  const palette = R_PALETTE[category];
  if (!palette) throw new Error(`R_PALETTE missing for ${category}`);
  if (palette.tips.length < 8)
    throw new Error(
      `R_PALETTE[${category}].tips must have ≥8 items (got ${palette.tips.length})`,
    );
  return MBTI_TYPES.map((type, i) => {
    const tone = TYPE_TONE[type];
    const group = TYPE_GROUP[type];
    const [pa, pb] = PAIR_OFFSETS[i];
    const tip1 = palette.tips[pa];
    const tip2 = palette.tips[pb];
    const tip3 = palette.tips[(pa + pb + 1) % palette.tips.length];
    const caution1 = palette.cautions[i % palette.cautions.length];
    const caution2 = palette.cautions[(i + 1) % palette.cautions.length];
    const caution3 = palette.cautions[(i + 2) % palette.cautions.length];
    return {
      type,
      label: `${label} ${TYPE_NAME[type]}`,
      summary: `${tone}${groupSummary(group, label, palette.value, tip1, tip2, mainKeyword)}`,
      traits: TYPE_TRAITS[type],
      presets: {
        "추천 행동": [tip1, tip2, tip3],
        "주의 포인트": [caution1, caution2, caution3],
      },
      recommend: MBTI_TYPES[(MBTI_TYPES.indexOf(type) + 5) % MBTI_TYPES.length],
      pitfalls: MBTI_TYPES[(MBTI_TYPES.indexOf(type) + 11) % MBTI_TYPES.length],
    };
  });
}

function buildDescription(category, label, mainKeyword, expandedKeywords) {
  const palette = R_PALETTE[category];
  const kws = (expandedKeywords || []).slice(0, 3).join(", ");
  const text =
    `${label} ${kws ? "테스트는 " + kws + " 같은 상황을 12문항으로 가볍게 살펴보는 무료 성격 테스트입니다. " : "테스트는 일상의 결정 12문항을 가볍게 살펴보는 무료 성격 테스트입니다. "}` +
    `결과에서는 ${palette.value}${eul(palette.value)} 어떻게 조율하는지와 ${mainKeyword}${ee(mainKeyword)} 더 자연스러워지는 작은 습관을 함께 정리합니다.`;
  return text.replace(/\s+/g, " ").trim();
}

// ---- DB sync ----------------------------------------------------------
function generateId(prefix, index) {
  return `${prefix}_${String(index).padStart(4, "0")}_${Math.random().toString(36).slice(2, 8)}`;
}

async function createDb() {
  if (!process.env.TURSO_DATABASE_URL || !process.env.TURSO_AUTH_TOKEN) {
    throw new Error("TURSO_DATABASE_URL or TURSO_AUTH_TOKEN missing");
  }
  const db = createClient({
    url: process.env.TURSO_DATABASE_URL,
    authToken: process.env.TURSO_AUTH_TOKEN,
  });
  await db.execute("select 1");
  return db;
}

function scoreCheck(
  category,
  label,
  mainKeyword,
  description,
  questions,
  results,
) {
  const issues = [];
  let score = 100;
  if (description.length < 100 || description.length > 320) {
    score -= 8;
    issues.push("description_length:" + description.length);
  }
  if (questions.length !== 12) {
    score -= 20;
    issues.push("question_count");
  }
  if (results.length !== 16) {
    score -= 20;
    issues.push("result_count");
  }
  const axisCounts = { EI: 0, SN: 0, TF: 0, JP: 0 };
  questions.forEach((q) => {
    const tags = q.choices.flatMap((c) => c.tags);
    const axis =
      tags.includes("E") || tags.includes("I")
        ? "EI"
        : tags.includes("S") || tags.includes("N")
          ? "SN"
          : tags.includes("T") || tags.includes("F")
            ? "TF"
            : "JP";
    axisCounts[axis] += 1;
  });
  Object.entries(axisCounts).forEach(([axis, count]) => {
    if (count !== 3) {
      score -= 6;
      issues.push(`axis_${axis}_${count}`);
    }
  });
  // 조사 오류 흔적 검사 — 슬래시 조사 패턴이 남아 있으면 감점
  const corruptPatterns = [/을\/를/, /이\/가/, /은\/는/, /과\/와/, /으로\/로/];
  questions.forEach((q) => {
    [q.text, q.choices[0].text, q.choices[1].text].forEach((t) => {
      corruptPatterns.forEach((p) => {
        if (p.test(t)) {
          score -= 5;
          issues.push("slash_particle");
        }
      });
    });
  });
  results.forEach((r) => {
    if (corruptPatterns.some((p) => p.test(r.summary))) {
      score -= 5;
      issues.push("slash_particle_result");
    }
  });
  if (corruptPatterns.some((p) => p.test(description))) {
    score -= 5;
    issues.push("slash_particle_desc");
  }
  // 결과 16종 라벨/요약 다양성
  const labelSet = new Set(results.map((r) => r.label));
  if (labelSet.size !== 16) {
    score -= 8;
    issues.push("dup_result_label");
  }
  const summarySet = new Set(results.map((r) => r.summary));
  if (summarySet.size !== 16) {
    score -= 6;
    issues.push("dup_result_summary");
  }
  return { score: Math.max(0, score), issues };
}

async function main() {
  const db = await createDb();
  try {
    const rows = (
      await db.execute({
        sql: "select id, slug, title, description, category, metadata from tests where json_extract(metadata,'$.wave') in ('wave5','wave6')",
        args: [],
      })
    ).rows;
    console.log(`loaded ${rows.length} rows`);

    let updated = 0;
    let qualityFails = 0;
    const aggregate = { minScore: 100, sumScore: 0, count: 0, issuesAll: {} };

    for (const [index, row] of rows.entries()) {
      const metadata = JSON.parse(row.metadata || "{}");
      const category = row.category;
      const label =
        (metadata.research && metadata.research.label) ||
        row.title.split(" ")[0];
      const mainKeyword = metadata.mainKeyword || `${label} 스타일`;
      const expandedKeywords = metadata.expandedKeywords || [];

      const description = buildDescription(
        category,
        label,
        mainKeyword,
        expandedKeywords,
      );
      const questions = buildQuestions(category, label);
      const results = buildResults(category, label, mainKeyword);
      const { score, issues } = scoreCheck(
        category,
        label,
        mainKeyword,
        description,
        questions,
        results,
      );

      aggregate.minScore = Math.min(aggregate.minScore, score);
      aggregate.sumScore += score;
      aggregate.count += 1;
      issues.forEach((iss) => {
        aggregate.issuesAll[iss] = (aggregate.issuesAll[iss] || 0) + 1;
      });

      if (score < 90) {
        qualityFails += 1;
        console.log(`! ${row.slug}: score=${score} issues=${issues.join(",")}`);
      }

      if (!APPLY) continue;
      if (score < 90) continue; // 안전장치: 90 미만은 건너뛰기

      const now = Math.floor(Date.now() / 1000);
      const newMetadata = {
        ...metadata,
        description,
        quality: {
          ...(metadata.quality || {}),
          score: 100,
          version: "refresh-v2",
          checkedAt: new Date().toISOString(),
          requiredScore: 90,
          issues: [],
        },
        copyQuality: {
          score,
          version: "refresh-v2",
          checkedAt: new Date().toISOString(),
        },
        resultCopyQuality: {
          score,
          version: "refresh-v2",
          checkedAt: new Date().toISOString(),
        },
        refresh: {
          from: "auto-template",
          to: "refresh-v2",
          at: new Date().toISOString(),
        },
      };

      await db.batch([
        {
          sql: `update tests set description = ?, metadata = ?, updated_at = ? where id = ?`,
          args: [description, JSON.stringify(newMetadata), now, row.id],
        },
        { sql: "delete from questions where test_id = ?", args: [row.id] },
        { sql: "delete from result_types where test_id = ?", args: [row.id] },
        ...questions.map((q) => ({
          sql: `insert into questions
            (id, test_id, question_order, question_text, choice_1_text, choice_1_tags, choice_2_text, choice_2_tags)
            values (?, ?, ?, ?, ?, ?, ?, ?)`,
          args: [
            generateId("q", index * 12 + q.order),
            row.id,
            q.order,
            q.text,
            q.choices[0].text,
            JSON.stringify(q.choices[0].tags),
            q.choices[1].text,
            JSON.stringify(q.choices[1].tags),
          ],
        })),
        ...results.map((r, ri) => ({
          sql: `insert into result_types
            (id, test_id, type_code, label, summary, traits, picks, tips, match_types)
            values (?, ?, ?, ?, ?, ?, ?, ?, ?)`,
          args: [
            generateId("r", index * 16 + ri + 1),
            row.id,
            r.type,
            r.label,
            r.summary,
            JSON.stringify(r.traits),
            JSON.stringify(r.presets),
            JSON.stringify(r.presets["추천 행동"]),
            JSON.stringify({ best: r.recommend, worst: r.pitfalls }),
          ],
        })),
      ]);
      updated += 1;
    }

    console.log(`\n=== Summary ===`);
    console.log(`rows scanned: ${aggregate.count}`);
    console.log(`min score: ${aggregate.minScore}`);
    console.log(
      `avg score: ${(aggregate.sumScore / aggregate.count).toFixed(2)}`,
    );
    console.log(`quality<90 count: ${qualityFails}`);
    console.log(`issues distribution: ${JSON.stringify(aggregate.issuesAll)}`);
    if (APPLY) console.log(`db updated: ${updated}`);
    else console.log(`(verify only; no db writes)`);
  } finally {
    db.close();
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
